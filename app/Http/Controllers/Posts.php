    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $user = $request->user();
        $posts = $user->isAdmin? Post::all(): Post::where('user_id',$user->id)->get();
        return view('posts.index', ['page' => 'posts.index', 'posts' => $posts]);
    }

